public class GeometryLab {
    
    public int f(int x, int y) {
        return x + y;
    }
    
    public int g(int x, int y) {
        return x - y;
    }
    
    public int h(int x, int y) {
        return x * y;
    }
    
    public int k(int x, int y) {
        return x % y;
    }

    public int j(int x, int y) {
        return x / y;
    }
    
    public int m(int x, int y, int z) {
        return x + y - z;
    }
    
}
